    private void btnCanjearPuntActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCanjearPuntActionPerformed

        //Declaración de variables locales
        int puntosDisp, puntosCanj;

        //Convierte texto de los campos de texto a enteros y los asigna a variables locales
        puntosDisp = Integer.parseInt(this.txtPuntosDisp.getText());
        puntosCanj = Integer.parseInt(this.txtPuntosCanjea.getText());

        //Condicional que se ejecuta en caso de tener menos de 400 puntos
        if (puntosDisp < 400) {
            //Muestra un mensaje en pantalla          
            JOptionPane.showMessageDialog(this,
                    "Puntos insuficientes",
                    "Alto",
                    JOptionPane.WARNING_MESSAGE);
        } else {
            //Condicional que se ejecuta en caso de que el destino indicado por el usuario sea "Panamá"
            if (this.pasajero.getDestino().equals("Panamá")) {
                //Se aplican los puntos
                this.txtPuntosCanjea.setText(Integer.toString(puntosCanj + 400));
                this.txtPuntosDisp.setText(Integer.toString(puntosDisp - 400));
                confirmacion = true; //Registro de que se aplicaron puntos
            } else {
                //Se aplican los puntos
                this.txtPuntosCanjea.setText(Integer.toString(puntosCanj + 500));
                this.txtPuntosDisp.setText(Integer.toString(puntosDisp - 500));
                confirmacion = true; //Registro de que se aplicaron puntos
            }
        }
    }//GEN-LAST:event_btnCanjearPuntActionPerformed


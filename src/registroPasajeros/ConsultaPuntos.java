    private void btnAceptarPuntActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarPuntActionPerformed

        //Condicional que se ejecuta en caso de que se hayan aplicados los puntos para registrar la transaccion en el objeto persona
        if (confirmacion == true) {
            this.pasajero.setTipoPago("Puntos");
            this.pasajero.setPuntosCanjeados();
        }

        //Muestra un mensaje en pantalla para indicar que se ingresaron los datos correctamente
        JOptionPane.showMessageDialog(this,
                "Datos ingresados correctamente!",
                "Éxito",
                JOptionPane.INFORMATION_MESSAGE);
        this.setVisible(false); //Se cierra la ventana
    }//GEN-LAST:event_btnAceptarPuntActionPerformed


